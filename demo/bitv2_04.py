import time
from webduino.webbit import WebBit

# 初始化
wbit = WebBit()

# 聖誕歌簡譜
jingle_bells = [[262, 0.25], [262, 0.25], [262, 0.5], [262, 0.25], [262, 0.25], [262, 0.5], [262, 0.25], [294, 0.25], [262, 0.5], [262, 0.25], [262, 0.25], [262, 0.5], [262, 0.25], [294, 0.25], [262, 0.5], [262, 0.25], [330, 0.5], [262, 0.5], [262, 0.5], [262, 0.5], [294, 0.5], [330, 0.5], [262, 0.5], [196, 0.5], [262, 0.5], [262, 0.5], [262, 0.5], [294, 0.5], [330, 0.5], [262, 0.5], [392, 0.5], [262, 0.5], [262, 0.5], [262, 0.5], [494, 0.5], [440, 0.25], [392, 0.25], [349, 0.25], [330, 0.25], [294, 0.25], [262, 0.25], [440, 0.25], [392, 0.25], [349, 0.25], [330, 0.25], [294, 0.25], [262, 0.25]]

# 燈光效果
def light_effect():
    for i in range(5):
        for j in range(5):
            wbit.show(i*5+j, 100, 0, 0)
            time.sleep(0.1)
            wbit.show(i*5+j, 0, 0, 0)

# 播放聖誕歌並加上燈光效果
while True:
    if wbit.btnA():
        wbit.play(jingle_bells)
        light_effect()
